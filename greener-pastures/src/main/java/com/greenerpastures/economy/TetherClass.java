package com.greenerpastures.economy;

/**
 * A Soul Tether's economic class. <b>Quality</b> (Shiny / IV / EV / Nature) = an <i>expense</i> — burns
 * high, buys non-Data value, sets the fuel:trophy grid ratio. <b>Throughput</b> (Speed / Enrichment /
 * Drops) = an <i>investment</i> — burns less than the value it adds, so it pays for itself.
 */
public enum TetherClass { QUALITY, THROUGHPUT }
